          <div class="row"> 
            <div class="col-md-12"><br><p><img style="float: left; margin: 0px 20px 20px 0px;" src="img/kwestionariusz.jpg" class="imagedropshadow">Kwestionariusz czytelniczy <strong>„In libris libertas”</strong> został przeze mnie stworzony z myślą o pacjentach ambulatoryjnych i hospitalizowanych, którzy są już objęci opieką terapeutyczną.
            <br><br>   
            Głównym celem kwestionariusza jest poznanie wzorców czytania pacjenta oraz jego motywacji do tego. Kwestionariusz może służyć również pomocą w planowaniu zajęć biblioterapeutycznych jako dodatkowej metody, prowadzonej obok głównego nurtu terapii.
            <br><br>    
            Kwestionariusz podzielony jest na cztery części. <br><br>

            <strong>1. Podstawowe informacje (11 pytań)</strong><br><br>

            Odpowiedzi udzielone na powyższe pytania dostarczają podstawowych danych demograficznych oraz informacji na temat choroby / zaburzenia, na które cierpi pacjent. Pytania dotyczące:<br><br>

            - płci;<br>
            - wieku<br>
            - miejsca zamieszkania<br>
            - wykształcenia<br>
            - grupy społeczno-zawodowej<br>
            - oceny własnej sytuacji materialnej<br><br>

            są tożsame z pytaniami zadawanymi podczas badania prowadzonego przez Bibliotekę Narodową, co może umożliwiać wstępne porównanie populacji osób cierpiących na specyficzne zaburzenia ze statystyczną, reprezentatywną próbką populacji.
            <br><br>
            <strong>2. Literatura w rodzinie pierwotnej (4 pytania)</strong><br><br>

            Pytania w tej części dotyczą wczesnych doświadczeń pacjenta z literaturą. Kierują uwagę osoby badanej w poszukiwaniu pierwszej książki, a także mogą być próbą odszukania dobrych obiektów z lat dziecięcych. Jakościowa analiza odpowiedzi będzie pomocna we wstępnym oszacowaniu podatności pacjenta na zajęcia biblioterapeutyczne.<br><br>

            <strong>3. Aktualne wzorce czytania (9 pytań)</strong><br><br>

            Pozycje w tej części mają za zadanie dostarczyć odpowiedzi na pytania:<br><br>

            - Czy osoby cierpiące na zaburzenia / choroby psychiczne czytają książki?<br>
            - Jeśli tak, to w jakie książki wybierają?<br>
            - Skąd osoba badana bierze książki?<br>
            - Jakie są największe przeszkody w czytaniu?<br><br>

            Ponownie, część pytań sformułowana jest w sposób tożsamy z pytaniami zadawanymi przez Bibliotekę Narodową, co może dawać wstępne oszacowanie ewentualnych różnic.<br><br>

            <strong>4. LRQ – Kwestionariusz reakcji czytelniczej (68 pytań)</strong><br><br>

            Kwestionariusz LRQ jest moją adaptacją narzędzia badawczego „Literary Response Questionnaire“ stworzonego w 1995 roku przez Davida S. Miall and Dona Kuiken. Adaptacja powstała za zgodą autorów. Kwestionariusz zawiera 7 skal głównych, opisanych poniżej. Narzędzie bada wewnętrzną motywację pacjenta do czytania, jego styl czytelniczy oraz wrażliwość na różne elementy dzieła literackiego. Narzędzie to może być również przydatne do oszacowania podatności pacjenta na oddziaływania biblioterapeutyczne.<br><br>

